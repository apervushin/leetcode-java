    public static int uniquePathsWithObstacles(int[][] obstacleGrid) {
        final int[][] dp = new int[obstacleGrid.length + 1][];
        dp[0] = new int[obstacleGrid[0].length + 1];
        for (int i = 1; i < dp.length; ++i) {
            dp[i] = new int[obstacleGrid[i - 1].length + 1];
            for (int j = 1; j < dp[i].length; ++j) {
                dp[i][j] = obstacleGrid[i - 1][j - 1] == 1 ? 0 : -1;
            }
        }
        dp[1][1] = dp[1][1] == -1 ? 1 : 0;

        for (int i = 1; i < dp.length; ++i) {
            for (int j = 1; j < dp[i].length; ++j) {
                if (dp[i][j] != -1) {
                    continue;
                }
                dp[i][j] = dp[i - 1][j] + dp[i][j - 1];
            }
        }

        return dp[dp.length - 1][dp[dp.length - 1].length - 1];
    }

